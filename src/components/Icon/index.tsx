// Export  icon
export const Icon: React.FC<IconProps> = ({ name, className, ...props }) => {
  if (name === 'es-flag') {
    return <img src={SpainFlagIcon} alt="es-flag" />;
  }

  // Get icon from icon map
  const Component = iconsMap[name];

  if (!Component) {
    return <div />;
  }
  // Return icon
  return <Component className={`d-icon ${className}`} {...props} />;
};
